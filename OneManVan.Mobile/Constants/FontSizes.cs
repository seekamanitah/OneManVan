namespace OneManVan.Mobile.Constants;

/// <summary>
/// Standardized font sizes for consistent typography throughout the app.
/// </summary>
public static class FontSizes
{
    /// <summary>10 - Tiny captions, helper text</summary>
    public const int Tiny = 10;
    
    /// <summary>12 - Secondary info, labels</summary>
    public const int Small = 12;
    
    /// <summary>14 - Body text, standard labels</summary>
    public const int Body = 14;
    
    /// <summary>16 - List items, form inputs</summary>
    public const int Medium = 16;
    
    /// <summary>18 - Subheadings</summary>
    public const int Large = 18;
    
    /// <summary>20 - Card titles</summary>
    public const int Title = 20;
    
    /// <summary>24 - Page headings</summary>
    public const int Heading = 24;
    
    /// <summary>32 - Hero text, display text</summary>
    public const int Display = 32;
}
