namespace OneManVan.Mobile.Constants;

/// <summary>
/// Standardized spacing values for consistent layout throughout the app.
/// Use these instead of magic numbers for padding, margin, and spacing.
/// </summary>
public static class Spacing
{
    /// <summary>4px - Tight spacing between closely related elements</summary>
    public const int Tiny = 4;
    
    /// <summary>8px - List item spacing, compact elements</summary>
    public const int Small = 8;
    
    /// <summary>12px - General spacing between elements</summary>
    public const int Medium = 12;
    
    /// <summary>16px - Card padding, section spacing (Default)</summary>
    public const int Large = 16;
    
    /// <summary>24px - Page margins, large separations</summary>
    public const int XLarge = 24;
    
    /// <summary>32px - Major section breaks</summary>
    public const int XXLarge = 32;
}
